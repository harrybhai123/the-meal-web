const item = document.getElementById("item");
const category = document.getElementById("category");
const area = document.getElementById("area");
const instructions = document.getElementById("instructions");
const img = document.getElementById("img");
const prev = document.getElementById("prev");
const random = document.getElementById("random");
const next = document.getElementById("next");

let current = 0;

window.addEventListener("DOMContentLoaded", function () {
  allMeal(current);
});

const allMeal = () => {
  let mealItems = items[current];
  item.textContent = "Meal Name:- " + mealItems.item;
  category.textContent = "Meal Category:- " + mealItems.category;
  area.textContent = "Meal Area:- " + mealItems.area;
  instructions.innerHTML =
    "<strong> Meal instructions:- </strong>" + mealItems.instructions;
  img.src = mealItems.MealThumb;
  console.log(mealItems.item);
};

random.addEventListener("click", function () {
  current = Math.trunc(Math.random() * items.length);
  allMeal(current);
});

prev.addEventListener("click", function () {
  current--;
  if (current < 0) {
    current = items.length - 1;
  }
  allMeal(current);
});
next.addEventListener("click", function () {
  current++;
  if (current > items.length - 1) {
    current = 0;
  }
  allMeal(current);
});
